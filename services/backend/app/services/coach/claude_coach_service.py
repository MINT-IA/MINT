def build_system_prompt(ctx: Optional[CoachContext] = None) -> str:
    """Build the full system prompt for the Claude coach.

    Args:
        ctx: Optional CoachContext with user-specific data. When provided,
             the prompt is enriched with the user's known values, archetype,
             and confidence score. When None, returns the base prompt only.

    Returns:
        The complete system prompt string to pass to the Anthropic API.
    """
    base = _BASE_SYSTEM_PROMPT.format(
        banned_terms=_BANNED_TERMS_REMINDER,
        regional_identity=_REGIONAL_IDENTITY,
        lifecycle_awareness=_LIFECYCLE_AWARENESS,
        plan_awareness=_PLAN_AWARENESS,
        routing_rules=_TOOL_ROUTING_RULES,
    )

    if ctx is None:
        return base

    # Enrich with user context
    context_section = _build_context_section(ctx)
    return base + "\n" + context_section


def _build_context_section(ctx: CoachContext) -> str:
    """Build the user-specific context section of the system prompt.

    Privacy: NEVER includes IBAN, full name, NPA, employer, or raw account
    numbers. Only aggregated financial indicators are passed.
    """
    known = ctx.known_values or {}

    lines = ["CONTEXTE UTILISATEUR (données agrégées, non identifiantes) :"]

    if ctx.archetype:
        lines.append(f"- Archétype : {ctx.archetype}")
    if ctx.age:
        lines.append(f"- Âge : {ctx.age} ans")
    if ctx.canton:
        lines.append(f"- Canton : {ctx.canton}")
    if ctx.fri_total is not None:
        lines.append(f"- Score FRI : {ctx.fri_total}/100")
    if ctx.fri_delta is not None:
        sign = "+" if ctx.fri_delta >= 0 else ""
        lines.append(f"- Variation FRI : {sign}{ctx.fri_delta:.0f} points")
    if ctx.primary_focus:
        lines.append(f"- Priorité actuelle : {ctx.primary_focus}")
    if ctx.replacement_ratio is not None:
        lines.append(f"- Taux de remplacement estimé : {ctx.replacement_ratio * 100:.0f}%")
    if ctx.months_liquidity is not None:
        lines.append(f"- Liquidités : {ctx.months_liquidity:.1f} mois de charges")
    if ctx.tax_saving_potential is not None:
        lines.append(f"- Potentiel d'économie fiscale : {ctx.tax_saving_potential:.0f} CHF")
    if ctx.confidence_score is not None:
        lines.append(f"- Confiance données : {ctx.confidence_score:.0f}%")
    if ctx.fiscal_season:
        lines.append(f"- Saison fiscale : {ctx.fiscal_season}")
    if ctx.upcoming_event:
        lines.append(f"- Événement à venir : {ctx.upcoming_event}")
    if ctx.check_in_streak:
        lines.append(f"- Streak de check-ins : {ctx.check_in_streak} jours")

    # Append extra known_values not already covered
    extra_keys = {
        k: v for k, v in known.items()
        if k not in {
            "fri_total", "replacement_ratio", "months_liquidity",
            "tax_saving", "confidence_score",
        }
        and v is not None
    }
    for key, value in extra_keys.items():
        lines.append(f"- {key} : {value}")

    lines.append(
        "\nNote : Ces données sont des estimations. "
        "Rappelle l'incertitude si tu les cites dans une projection."
    )

    return "\n".join(lines)
